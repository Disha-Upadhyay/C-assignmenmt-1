// Filename: disha_arraytools.h
#ifndef DISHA_ARRAYTOOLS_H
#define DISHA_ARRAYTOOLS_H

// Prints all elements of the array
void printArray(int arr[], int n);

// Reverses the array elements
void reverseElements(int arr[], int n);

// Returns the index of the largest element
int indexOfMax(int arr[], int n);

// Returns the index of the smallest element
int indexOfMin(int arr[], int n);

// Calculates and returns the average of array elements
float averageOfArray(int arr[], int n);

// Searches for a value using linear search and returns its index
int searchInArray(int arr[], int n, int key);

#endif
